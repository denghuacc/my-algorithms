// @lc code=start
/**
 * 方法一：双指针（分段扫描）
 *
 * 思路：
 * 1. 先跳过开头那一段连续的 '1'；
 * 2. 再跳过后续连续的 '0'；
 * 3. 如果此时已经到达字符串末尾，说明最多只有一段 '1'。
 *
 * @param s - 不含前导零的二进制字符串
 * @returns 是否最多只包含一段连续的 '1'
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)
 */
var checkOnesSegment = function (s: string): boolean {
  const n = s.length;
  let i = 0;

  // 跳过第一段连续的 '1'
  while (i < n && s[i] === "1") {
    i++;
  }

  // 跳过后续连续的 '0'
  while (i < n && s[i] === "0") {
    i++;
  }

  // 若后面还有字符，只可能是再次出现的 '1'，则字段数超过 1 段
  return i === n;
};

/**
 * 方法二：字符串匹配（最简写法）
 *
 * 关键观察：
 * 若存在至少两段 '1'，则必然出现子串 "01"（第一段 1 结束并转到 0，
 * 后面又要回到 1）。
 * 反之，不存在 "01" 则最多只有一段连续的 '1'。
 *
 * @param s - 不含前导零的二进制字符串
 * @returns 是否最多只包含一段连续的 '1'
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)（忽略实现细节）
 */
var checkOnesSegment = function (s: string): boolean {
  return !s.includes("01");
};
// @lc code=end

/*
解题思路详解：

1. 题目理解
   - 问题本质：
     判断字符串中由连续 '1' 组成的“字段”数量是否不超过 1。
   - 关键条件：
     1) 字符串只含 '0' 和 '1'。
     2) 题目保证没有前导零（即 s[0] 一定是 '1'）。
   - 目标：
     返回布尔值，不需要统计精确字段数。

2. 方法一：双指针（分段扫描）

   核心思想：
   - 如果最多只有一段 '1'，字符串形态一定是：
     `111...11000...00`（前面一段 1，后面全是 0，可没有 0）。
   - 于是按顺序扫描：
     1) 先吃掉第一段 1；
     2) 再吃掉后续的 0；
     3) 若能正好走到结尾，说明没有第二段 1。

   算法步骤：
   1. 指针 i 从 0 开始，跳过连续 '1'。
   2. 继续跳过连续 '0'。
   3. 判断 i 是否等于 n：
      - 等于 n：最多一段 '1'，返回 true
      - 小于 n：后面又出现 '1'，返回 false

   复杂度分析：
   - 时间复杂度：O(n)，每个字符最多访问一次。
   - 空间复杂度：O(1)。

3. 方法二：字符串匹配（推荐）

   核心观察：
   - “超过一段 1” 的必要且充分条件是出现模式 "01"。
   - 解释：
     1) 第一段 1 结束时会进入 0；
     2) 若后面还有第二段 1，必然从 0 再切回 1；
     3) 这两步合起来一定包含 "01"。

   因此：
   - `s.includes("01") === true` -> 至少两段 1 -> 返回 false
   - `s.includes("01") === false` -> 最多一段 1 -> 返回 true

   复杂度分析：
   - 时间复杂度：O(n)。
   - 空间复杂度：O(1)（按题解层面记法）。

4. 示例分析

   示例 1：s = "1001"
   - 存在 "01"（中间的 00 后又回到 1）
   - 说明有两段 1（"1" 和 "1"）
   - 返回 false

   示例 2：s = "110"
   - 不存在 "01"
   - 只有一段 1（"11"）
   - 返回 true

   示例 3：s = "1"
   - 只有一段 1
   - 返回 true

   示例 4：s = "1111000"
   - 前面一段 1，后面全 0
   - 返回 true

5. 常见错误
   - 误把 "10" 当成判定条件。真正关键是是否出现 "01"。
   - 忘记题目保证无前导零，导致额外处理分支过多。
   - 双指针实现时未做边界判断（`i < n`），可能越界访问。

6. 总结
   - 这题最简做法是判断是否包含 "01"。
   - 若用于教学，双指针分段扫描更直观，能体现字段结构。
   - 两种方法都可达 O(n) 时间，工程上推荐方法二。
*/
