/**
 * 通过递归分解 + 重排，构造字典序最大的特殊二进制序列。
 *
 * @param s - 特殊二进制序列
 * @returns 经过任意次合法交换后可得到的最大字典序字符串
 */
function makeLargestSpecial(s: string): string {
  // 长度 <= 2 时，字符串只能是 "10"，已经是最优
  if (s.length <= 2) {
    return s;
  }
  let balance = 0;
  let segmentStart = 0;
  const segments: string[] = [];

  // 按平衡括号思想切分：1 视作 '('，0 视作 ')'
  for (let i = 0; i < s.length; i++) {
    if (s[i] === "1") {
      balance++;
    } else {
      balance--;
      // balance 回到 0，说明找到一个最外层特殊子串 [segmentStart..i]
      if (balance === 0) {
        // 去掉外层 "1...0"，递归优化内部，再包回去
        segments.push(
          "1" + makeLargestSpecial(s.slice(segmentStart + 1, i)) + "0"
        );
        segmentStart = i + 1;
      }
    }
  }

  // 为了全局字典序最大，将同层子串按降序排列后拼接
  segments.sort((a, b) => b.localeCompare(a));
  return segments.join("");
}
