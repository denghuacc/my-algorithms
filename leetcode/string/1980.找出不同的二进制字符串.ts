// @lc code=start
/**
 * 找出一个不在 nums 中的长度为 n 的二进制字符串。
 *
 * 核心思路（康托对角线构造）：
 * 构造答案 ans，使得 ans[i] 与 nums[i][i] 一定不同。
 * 这样 ans 至少在第 i 位与 nums[i] 不同，
 * 因此不可能等于 nums 中任意字符串。
 *
 * @param nums - n 个互不相同、且每个长度都为 n 的二进制字符串
 * @returns 任意一个未出现在 nums 中的长度为 n 的二进制字符串
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(n)
 */
function findDifferentBinaryString(nums: string[]): string {
  const n = nums.length;
  const ans: string[] = new Array(n);

  // 对角线构造：第 i 位取 nums[i][i] 的相反值。
  for (let i = 0; i < n; i++) {
    ans[i] = nums[i][i] === "0" ? "1" : "0";
  }

  return ans.join("");
}
// @lc code=end

/*
解题思路详解：

1. 题目理解
   - 问题本质：
     给定 n 个长度为 n 的互异二进制串，构造一个“缺失串”。
   - 关键特点：
     1) 每个字符串长度恰好是 n。
     2) 只需返回任意一个可行答案，不要求字典序最小。
   - 目标：
     用尽量低的时间复杂度构造出不在 nums 中的字符串。

2. 解题思路

   核心思想：
   - 使用康托对角线思想构造答案 `ans`：
     `ans[i] = flip(nums[i][i])`，即把对角线字符按位取反。
   - 其中 `flip('0')='1'`，`flip('1')='0'`。

   为什么一定正确：
   - 对任意下标 i，`ans[i]` 与 `nums[i][i]` 必不相同。
   - 因此 `ans` 与 `nums[i]` 在第 i 位一定不同。
   - 所以 `ans` 不可能与任何一个 `nums[i]` 完全相等。
   - 结论：`ans` 一定不在 nums 中。

   算法步骤：
   1. 初始化长度为 n 的字符数组 `ans`。
   2. 遍历 i 从 0 到 n-1：
      - 若 `nums[i][i]` 为 '0'，则 `ans[i]='1'`
      - 否则 `ans[i]='0'`
   3. 把 `ans` 拼接为字符串并返回。

3. 代码实现说明
   - 仅访问每个字符串的一个字符（第 i 个字符串的第 i 位）。
   - 不需要构建哈希集合，也不需要枚举 `2^n` 个候选串。
   - 代码简短且证明直接，适合本题。

4. 复杂度分析
   - 时间复杂度：O(n)
     只做一次长度为 n 的循环。
   - 空间复杂度：O(n)
     需要存储答案字符数组。

5. 示例分析

   示例 1：
   nums = ["01","10"]
   - i=0：nums[0][0]='0' -> ans[0]='1'
   - i=1：nums[1][1]='0' -> ans[1]='1'
   - ans = "11"，不在 nums 中。

   示例 2：
   nums = ["00","01"]
   - i=0：'0' -> '1'
   - i=1：'1' -> '0'
   - ans = "10"，不在 nums 中。

   示例 3：
   nums = ["111","011","001"]
   - 对角线是 ['1','1','1']
   - 取反后 ans = "000"
   - 与 nums[0] 第 0 位不同、与 nums[1] 第 1 位不同、与 nums[2] 第 2 位不同，
     因此一定不在数组中。

6. 常见错误
   - 把取反写反（'0' 仍写成 '0'）。
   - 对角线下标写错成 `nums[i][0]` 或 `nums[0][i]`。
   - 误以为必须返回唯一答案或字典序最小答案。

7. 扩展思考
   - 枚举 + 哈希也是可行解，但时间复杂度是 O(n * 2^n)。
   - 本题由于“数量 n 与长度 n 对齐”，对角线法能直接做到 O(n)。
*/
