/**
 * 生成交替二进制字符串的最少操作数
 *
 * 核心思路：
 * 交替串只有两种合法形态：
 * 1) 以 '0' 开头：010101...
 * 2) 以 '1' 开头：101010...
 * 统计其中一种形态的失配数，另一种形态失配数可由 n - mismatch 得到。
 *
 * @param s - 仅由 '0' 和 '1' 组成的字符串
 * @returns 把 s 变成交替字符串的最少翻转次数
 *
 * 时间复杂度：O(n)
 * 空间复杂度：O(1)
 */
function minOperations(s: string): number {
  const n = s.length;
  let mismatchStartWithZero = 0;

  // 统计与“以 0 开头的交替串”相比，当前位置是否失配。
  // 目标模板：偶数位是 '0'，奇数位是 '1'。
  for (let i = 0; i < n; i++) {
    const expected = i % 2 === 0 ? "0" : "1";
    if (s[i] !== expected) {
      mismatchStartWithZero++;
    }
  }

  // 另一种模板（以 1 开头）的失配数正好是 n - mismatchStartWithZero。
  // 取两者较小值即最少操作数。
  return Math.min(mismatchStartWithZero, n - mismatchStartWithZero);
}
