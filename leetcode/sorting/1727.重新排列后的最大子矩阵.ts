/**
 * 重新排列列后，求全 1 子矩阵的最大面积。
 *
 * 核心思路：
 * 1. 先把每个位置看成“以该行为底时，该列连续 1 的高度”。
 * 2. 由于每一行都允许对整列重新排序，等价于：
 *    在固定某一行作为底边时，可以把这一行的所有高度按从大到小排列。
 * 3. 排序后，若选择前 k 列作为矩形宽度，
 *    则矩形高度由第 k 列的高度决定，
 *    面积为 `sortedHeights[k - 1] * k`。
 *
 * @param matrix - 二进制矩阵
 * @returns 重新排列列之后，全 1 子矩阵的最大面积
 *
 * 时间复杂度：O(m * n * log n)
 * 空间复杂度：O(1)（原地修改输入矩阵）
 */
function largestSubmatrix(matrix: number[][]): number {
  const m = matrix.length;
  const n = matrix[0].length;
  let maxArea = 0;

  // 预处理连续高度：
  // matrix[i][j] 最终表示“以第 i 行为底，该列向上连续 1 的高度”。
  for (let i = 1; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (matrix[i][j] === 1) {
        matrix[i][j] += matrix[i - 1][j];
      }
    }
  }

  // 对每一行单独考虑：
  // 因为列可重排，所以把当前行的各列高度降序排列后，
  // 枚举“取前 j+1 列作为宽度”即可。
  for (let i = 0; i < m; i++) {
    matrix[i].sort((a, b) => b - a);
    for (let j = 0; j < n; j++) {
      maxArea = Math.max(maxArea, matrix[i][j] * (j + 1));
    }
  }
  return maxArea;
}
