/**
 * 构造满足按位或条件的最小数组。
 *
 * @param nums - 由质数组成的数组
 * @returns 按位或条件下的最小数组
 */
function minBitwiseArray(nums: number[]): number[] {
  const n = nums.length;
  for (let i = 0; i < n; i++) {
    const x = nums[i];
    let res = -1;
    let d = 1;
    // 找到 x 的最低连续 1 段，并取该段中最高位 1 作为减去的值
    while ((x & d) !== 0) {
      res = x - d;
      d <<= 1;
    }
    nums[i] = res;
  }
  return nums;
}
