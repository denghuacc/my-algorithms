/**
 * 计算二进制表示中相邻两个 1 的最大距离。
 *
 * @param n - 正整数
 * @returns 相邻 1 的最大距离，不存在则返回 0
 */
function binaryGap(n: number): number {
  // lastOnePos: 上一个 1 出现的位下标（从低位 0 开始）
  let lastOnePos = -1;
  let maxDistance = 0;

  // 从低位到高位扫描每一位
  for (let i = 0; n !== 0; i++) {
    if ((n & 1) === 1) {
      // 若之前出现过 1，更新相邻 1 之间的距离
      if (lastOnePos !== -1) {
        maxDistance = Math.max(maxDistance, i - lastOnePos);
      }
      lastOnePos = i;
    }
    // 无符号右移，继续检查下一位
    n >>>= 1;
  }
  return maxDistance;
}
