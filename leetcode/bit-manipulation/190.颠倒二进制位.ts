// @lc code=start
// bit-manipulation
/**
 * 解法一：逐位提取并放到镜像位置。
 *
 * @param n - 32 位无符号整数（以 number 表示）
 * @returns 二进制位反转后的无符号整数
 */
var reverseBits = function (n: number): number {
  let rev = 0;
  for (let i = 0; i < 32; i++) {
    // 取出 n 的最低位，移动到 rev 的对称位置
    rev |= (n & 1) << (31 - i);
    // 无符号右移，继续处理下一位
    n >>>= 1;
  }
  // 转为无符号 32 位结果
  return rev >>> 0;
};

// divide and conquer
/**
 * 解法二：分治交换位块（1/2/4/8/16 位块两两交换）。
 *
 * @param n - 32 位无符号整数（以 number 表示）
 * @returns 二进制位反转后的无符号整数
 */
var reverseBits = function (n: number): number {
  const M1 = 0x55555555; // 01010101010101010101010101010101
  const M2 = 0x33333333; // 00110011001100110011001100110011
  const M4 = 0x0f0f0f0f; // 00001111000011110000111100001111
  const M8 = 0x00ff00ff; // 00000000111111110000000011111111

  // 交换相邻 1 位块
  n = ((n >>> 1) & M1) | ((n & M1) << 1);
  // 交换相邻 2 位块
  n = ((n >>> 2) & M2) | ((n & M2) << 2);
  // 交换相邻 4 位块
  n = ((n >>> 4) & M4) | ((n & M4) << 4);
  // 交换相邻 8 位块
  n = ((n >>> 8) & M8) | ((n & M8) << 8);
  // 交换相邻 16 位块，完成 32 位反转
  return ((n >>> 16) | (n << 16)) >>> 0;
};
