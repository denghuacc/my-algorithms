/**
 * 计算把二进制字符串 s 通过“偶数除 2 / 奇数加 1”变为 1 的最少步骤数。
 *
 * @param s - 二进制字符串，最高位保证为 '1'
 * @returns 变为 1 所需的操作次数
 */
function numSteps(s: string): number {
  // 特判：已经是 1，不需要任何操作。
  if (s === "1") {
    return 0;
  }

  const n = s.length;
  let steps = 0;

  // carry 表示“低位处理后向更高位产生的进位”。
  // 0 表示无进位，1 表示有进位。
  let carry = 0;

  // 只遍历到下标 1，最高位（下标 0）单独处理。
  // 原因：最终目标是变成 1，最高位的处理与普通位略有不同。
  for (let i = n - 1; i > 0; i--) {
    // 当前位（0/1）叠加低位传来的进位后，可能是 0、1、2。
    const bit = s.charCodeAt(i) - 48;
    const value = bit + carry;

    if (value === 1) {
      // value=1 表示当前是奇数位状态：
      // 需要两步：
      // 1) +1 把奇数变偶数
      // 2) /2 右移一位
      steps += 2;

      // 做了 +1 之后，必然向更高位产生进位。
      carry = 1;
    } else {
      // value=0 或 2 都是偶数状态：
      // 只需要一步 /2。
      steps += 1;
    }
  }

  // 处理最高位：
  // 若还有进位，最高位会从 1 变成 10，还需要再做一次 /2。
  // 因此额外 +carry。
  return steps + carry;
}
