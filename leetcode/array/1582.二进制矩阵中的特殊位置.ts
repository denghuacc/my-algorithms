/**
 * 统计二进制矩阵中的特殊位置数量。
 *
 * 特殊位置定义：
 * - mat[i][j] === 1
 * - 第 i 行中 1 的数量为 1
 * - 第 j 列中 1 的数量为 1
 *
 * @param mat - rows x cols 的二进制矩阵
 * @returns 特殊位置总数
 *
 * 时间复杂度：O(rows * cols)
 * 空间复杂度：O(rows + cols)
 */
function numSpecial(mat: number[][]): number {
  const rows = mat.length;
  const cols = mat[0].length;
  const rowSum: number[] = new Array(rows).fill(0);
  const colSum: number[] = new Array(cols).fill(0);

  // 第一次遍历：分别统计每一行、每一列中 1 的数量。
  for (let i = 0; i < rows; i++) {
    for (let j = 0; j < cols; j++) {
      rowSum[i] += mat[i][j];
      colSum[j] += mat[i][j];
    }
  }

  let specialCount = 0;

  // 第二次遍历：只有行和为 1 的行才可能包含特殊位置，
  // 其他行可直接跳过。
  for (let i = 0; i < rows; i++) {
    if (rowSum[i] !== 1) {
      continue;
    }

    for (let j = 0; j < cols; j++) {
      // 当前位置为 1 且对应列和也为 1，则该点是特殊位置。
      if (mat[i][j] === 1 && colSum[j] === 1) {
        specialCount++;
      }
    }
  }

  return specialCount;
}
