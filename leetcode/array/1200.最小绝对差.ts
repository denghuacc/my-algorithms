/**
 * 返回所有最小绝对差的数对（升序）。
 *
 * @param arr - 互不相同的整数数组
 * @returns 最小绝对差的数对列表
 */
function minimumAbsDifference(arr: number[]): number[][] {
  const n = arr.length;
  // 排序后，最小绝对差一定发生在相邻元素之间
  arr.sort((a, b) => a - b);
  let res: number[][] = [];
  let minDiff = Infinity;
  for (let i = 1; i < n; i++) {
    const diff = arr[i] - arr[i - 1];
    if (diff < minDiff) {
      res = [[arr[i - 1], arr[i]]];
      minDiff = diff;
    } else if (diff === minDiff) {
      res.push([arr[i - 1], arr[i]]);
    }
  }
  return res;
}
