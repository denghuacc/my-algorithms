/**
 * 模拟棒球分数记录的过程，按顺序处理每条操作指令。
 *
 * @param ops - 操作列表，可能是整数或 "C" | "D" | "+"
 * @returns 返回所有有效得分的总和
 */
function calPoints(ops: string[]): number {
  const points: number[] = [];
  let sum = 0;
  for (const op of ops) {
    const n = points.length;
    switch (op) {
      case "C": {
        const last = points[n - 1]; // 取出最后一次有效得分
        sum -= last; // 撤销得分需要从总和里扣除
        points.pop(); // 同时从记录表中删除
        break;
      }
      case "D": {
        const double = points[n - 1] * 2; // 按规则翻倍上一轮得分
        sum += double;
        points.push(double);
        break;
      }
      case "+": {
        const pls = points[n - 1] + points[n - 2]; // 最近两次得分求和
        sum += pls;
        points.push(pls);
        break;
      }
      default: {
        const num = parseInt(op, 10); // 常规整数得分
        sum += num;
        points.push(num);
      }
    }
  }
  return sum;
}
