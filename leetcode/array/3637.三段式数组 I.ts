/**
 * 判断数组是否为三段式：先严格递增，再严格递减，再严格递增。
 *
 * @param nums - 整数数组
 * @returns 是否为三段式数组
 */
function isTrionic(nums: number[]): boolean {
  const n = nums.length;
  let i = 1;
  // 第一段：严格递增
  while (i < n && nums[i] > nums[i - 1]) {
    i++;
  }
  const p = i - 1;
  if (p === 0 || p === n - 1) {
    // 第一段至少包含两个元素，且不能到达末尾
    return false;
  }
  // 第二段：严格递减
  while (i < n && nums[i] < nums[i - 1]) {
    i++;
  }
  const q = i - 1;
  if (q === p || q === n - 1) {
    // 第二段至少包含两个元素，且不能到达末尾
    return false;
  }
  // 第三段：严格递增
  while (i < n && nums[i] > nums[i - 1]) {
    i++;
  }
  // 必须完全遍历到末尾才算满足三段式
  return i === n;
}
