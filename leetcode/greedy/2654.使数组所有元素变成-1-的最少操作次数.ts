/**
 * 使数组所有元素变成 1 的最少操作次数
 *
 * 核心思路：
 * 1. 如果数组中已有 1，直接用这个 1 去"传染"其他元素（n - num1 次操作）
 * 2. 如果数组整体的 GCD 不为 1，无论如何操作都无法得到 1，返回 -1
 * 3. 否则，先找到最短的子数组使其 GCD 为 1，然后用这个 1 去传染其他元素
 */
function minOperations(nums: number[]): number {
  const n = nums.length;
  let num1 = 0; // 统计数组中 1 的个数
  let g = 0; // 整个数组的最大公约数

  // 第一次遍历：统计 1 的个数，并计算所有元素的 GCD
  for (const num of nums) {
    if (num === 1) {
      num1++;
    }
    // 逐步累积计算所有元素的 GCD
    g = gcd(g, num);
  }

  // 情况1：如果数组中已经有 1 存在
  // 任何数与 1 的 GCD 都是 1，所以可以用已有的 1 依次将其他元素变成 1
  // 需要 n - num1 次操作（将不是 1 的元素都变成 1）
  if (num1 > 0) {
    return n - num1;
  }

  // 情况2：如果所有元素的 GCD 不为 1
  // 那么无论如何操作，得到的结果始终是这个 GCD 的倍数，永远无法得到 1
  if (g !== 1) {
    return -1;
  }

  // 情况3：数组中没有 1，但所有元素的 GCD 为 1
  // 需要找到最短的连续子数组，使其 GCD 为 1
  let minLen = Infinity;

  // 枚举所有可能的子数组起点
  for (let i = 0; i < n; i++) {
    let currentGcd = 0;
    // 从起点 i 开始，逐步扩展子数组
    for (let j = i; j < n; j++) {
      // 计算当前子数组 [i, j] 的 GCD
      currentGcd = gcd(currentGcd, nums[j]);
      // 一旦找到 GCD 为 1 的子数组，记录其长度
      if (currentGcd === 1) {
        minLen = Math.min(minLen, j - i + 1);
        break; // 已经找到从 i 开始的最短子数组，无需继续扩展
      }
    }
  }

  // 总操作次数 = 将最短子数组变成 1 的操作次数 + 用这个 1 传染其他元素的操作次数
  // - 将长度为 minLen 的子数组变成 1：需要 (minLen - 1) 次操作
  //   （每次操作减少一个非 1 元素）
  // - 用这个 1 传染剩余的 (n - 1) 个元素：需要 (n - 1) 次操作
  // 总计：(minLen - 1) + (n - 1) = n - 1 + minLen - 1
  return n - 1 + (minLen - 1);

  /**
   * 计算两个数的最大公约数（欧几里得算法）
   * @param a - 第一个数
   * @param b - 第二个数
   * @returns 最大公约数
   */
  function gcd(a: number, b: number): number {
    return b === 0 ? a : gcd(b, a % b);
  }
}
