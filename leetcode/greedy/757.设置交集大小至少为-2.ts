// @lc code=start
/**
 * 贪心：倒序扫描区间，从左往右放最小值，方便前序区间复用。
 *
 * @param intervals - 闭区间 [start, end] 的数组。
 * @returns 使每个区间至少包含两个点的最小集合大小。
 */
function intersectionSizeTwo(intervals: number[][]): number {
  const n = intervals.length;
  const REQUIRED = 2;
  let size = 0;

  intervals.sort((a, b) => {
    if (a[0] === b[0]) {
      return b[1] - a[1];
    }
    return a[0] - b[0];
  });

  const sharedValues = Array.from({ length: n }, () => [] as number[]);

  // 先处理起点最大的区间，后面选的点还能被前面的区间复用。
  for (let i = n - 1; i >= 0; i--) {
    for (
      let value = intervals[i][0], need = sharedValues[i].length;
      need < REQUIRED;
      need++, value++
    ) {
      size++;
      // 把新选的点写入所有仍覆盖它的前序区间，供后续计数复用。
      for (let p = i - 1; p >= 0 && intervals[p][1] >= value; p--) {
        sharedValues[p].push(value);
      }
    }
  }

  return size;
}
