// @lc code=start
/**
 * 枚举所有子数组，统计不同偶数与奇数的数量，取最长长度。
 *
 * @param nums - 整数数组
 * @returns 最长平衡子数组长度
 */
function longestBalanced(nums: number[]): number {
  let maxLength = 0;
  const n = nums.length;
  for (let i = 0; i < n; i++) {
    // 每次固定左端点 i，向右扩展子数组
    const evenSet = new Set<number>();
    const oddSet = new Set<number>();
    for (let j = i; j < n; j++) {
      if (nums[j] % 2 === 0) {
        // 记录不同偶数
        evenSet.add(nums[j]);
      } else {
        // 记录不同奇数
        oddSet.add(nums[j]);
      }
      if (evenSet.size === oddSet.size) {
        // 平衡时更新最大长度
        maxLength = Math.max(maxLength, j - i + 1);
      }
    }
  }
  return maxLength;
}
