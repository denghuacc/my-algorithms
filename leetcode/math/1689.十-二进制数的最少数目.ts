/**
 * 十-二进制数的最少数目
 *
 * 核心结论：
 * 和为 n 的十-二进制数最少数量，等于 n 中最大的那一位数字。
 *
 * @param n - 十进制正整数字符串（无前导零）
 * @returns 最少需要的十-二进制数数量
 *
 * 时间复杂度：O(n.length)
 * 空间复杂度：O(1)
 */
function minPartitions(n: string): number {
  const ZERO_CODE = "0".charCodeAt(0);
  let maxDigit = 0;

  // 扫描每一位，维护当前最大数字。
  // 十-二进制数每一位最多贡献 1，因此某一位为 d 时至少要 d 个数。
  for (const ch of n) {
    const digit = ch.charCodeAt(0) - ZERO_CODE;
    maxDigit = Math.max(maxDigit, digit);

    // 数字位最大只可能到 9，达到上界可直接返回。
    if (maxDigit === 9) {
      return 9;
    }
  }

  return maxDigit;
}
